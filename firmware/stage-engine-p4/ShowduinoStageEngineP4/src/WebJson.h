#ifndef SHOWDUINO_WEB_JSON_H
#define SHOWDUINO_WEB_JSON_H

#include <Arduino.h>

namespace ShowduinoWebJson {

inline String escape(const String &in) {
  String out;
  out.reserve(in.length() + 8);
  for (size_t i = 0; i < in.length(); i++) {
    char c = in[i];
    if (c == '\\' || c == '"') {
      out += '\\';
      out += c;
    } else if (c == '\n') {
      out += "\\n";
    } else if (c == '\r') {
      out += "\\r";
    } else if (c == '\t') {
      out += "\\t";
    } else {
      out += c;
    }
  }
  return out;
}

}  // namespace ShowduinoWebJson

#endif
